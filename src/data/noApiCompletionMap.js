export const NO_API_COMPLETION_TOTAL = 132;

export function flattenNoApiCompletionMap() {
  return NO_API_COMPLETION_DOMAINS.flatMap((domain, domainIndex) => (
    domain.builds.map((label, buildIndex) => ({
      id: `${String(domainIndex + 1).padStart(2, '0')}-${domain.id}-${String(buildIndex + 1).padStart(2, '0')}`,
      domain: domain.id,
      domainLabel: domain.label,
      label,
      priority: buildIndex < 4 ? 'critical' : buildIndex < 8 ? 'major' : 'polish',
      phase: domainIndex < 4 ? 'core' : domainIndex < 8 ? 'ops' : 'scale',
      position: buildIndex + 1,
    }))
  ));
}

export function buildNoApiCompletionSnapshot() {
  const builds = flattenNoApiCompletionMap();
  const domains = NO_API_COMPLETION_DOMAINS.map((domain) => {
    const domainBuilds = builds.filter((build) => build.domain === domain.id);
    return {
      ...domain,
      total: domainBuilds.length,
      critical: domainBuilds.filter((build) => build.priority === 'critical').length,
      major: domainBuilds.filter((build) => build.priority === 'major').length,
      polish: domainBuilds.filter((build) => build.priority === 'polish').length,
      builds: domainBuilds,
    };
  });

  return {
    total: builds.length,
    expectedTotal: NO_API_COMPLETION_TOTAL,
    domains,
    builds,
    critical: builds.filter((build) => build.priority === 'critical').length,
    major: builds.filter((build) => build.priority === 'major').length,
    polish: builds.filter((build) => build.priority === 'polish').length,
    phases: {
      core: builds.filter((build) => build.phase === 'core').length,
      ops: builds.filter((build) => build.phase === 'ops').length,
      scale: builds.filter((build) => build.phase === 'scale').length,
    },
  };
}
