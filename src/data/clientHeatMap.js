export function scoreNeighborhood(neighborhood, lens = 'overall') {
  const weights = WEIGHTS[lens] || WEIGHTS.overall;
  const wellnessDemand = neighborhood.wellnessSpend * 0.65 + neighborhood.luxuryFitness * 0.35;
  const spendIncome = Math.round((neighborhood.affluence * wellnessDemand) / 100);
  const score = Object.entries(weights).reduce((sum, [key, weight]) => {
    const value = key === 'spendIncome' ? spendIncome : neighborhood[key];
    return sum + value * weight;
  }, 0);
  return Math.round(score);
}

export function rankNeighborhoods(lens = 'overall') {
  return [...clientHeatMapNeighborhoods]
    .map((neighborhood) => ({
      ...neighborhood,
      wellnessDemand: Math.round(neighborhood.wellnessSpend * 0.65 + neighborhood.luxuryFitness * 0.35),
      spendIncome: Math.round((neighborhood.affluence * (neighborhood.wellnessSpend * 0.65 + neighborhood.luxuryFitness * 0.35)) / 100),
      score: scoreNeighborhood(neighborhood, lens),
    }))
    .sort((a, b) => b.score - a.score);
}
